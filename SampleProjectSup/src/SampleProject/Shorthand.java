//Short hand operator and printing value of a variable using FOR LOOP

package SampleProject;

public class Shorthand {
	
	public static void main(String args[])
	{
		int i=0;
		
		for(i=0;i<=10;i++)
		{
		System.out.printf("Value of i is " +i +"\n");
		}
		i+=2;  //i=i+2 - This is called short hand operator
	}
	

}
